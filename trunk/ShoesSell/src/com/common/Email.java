package com.common;

public class Email {
	private String smtpServSrc ;
	private String addressSrc ;
	private String usernameSrc ;
	private String passwordSrc ;
	private String addressStd ;
	private String title ;
	private String content ;

	public String getSmtpServSrc() {
		return smtpServSrc;
	}
	public void setSmtpServSrc(String smtpServSrc) {
		this.smtpServSrc = smtpServSrc;
	}
	public String getAddressSrc() {
		return addressSrc;
	}
	public void setAddressSrc(String addressSrc) {
		this.addressSrc = addressSrc;
	}
	public String getUsernameSrc() {
		return usernameSrc;
	}
	public void setUsernameSrc(String usernameSrc) {
		this.usernameSrc = usernameSrc;
	}
	public String getPasswordSrc() {
		return passwordSrc;
	}
	public void setPasswordSrc(String passwordSrc) {
		this.passwordSrc = passwordSrc;
	}
	public String getAddressStd() {
		return addressStd;
	}
	public void setAddressStd(String addressStd) {
		this.addressStd = addressStd;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	

}
